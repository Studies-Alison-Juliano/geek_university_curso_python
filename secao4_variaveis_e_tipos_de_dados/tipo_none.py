"""
Tipo none

O tipo de dado none em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porem, falar
que é um tipo sem tipo é mais apropriado

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Quando utilizamos ?
- Podemos utilizar None quanto queremos criar uma variável e inicializa-la com um tipo sem tipo, antes de recebermos um
valor final

OBS: O tipo None em Python é SEMPRE considerado como falso
"""
numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 2.22, 3.44
print(numeros)
print(type(numeros))
